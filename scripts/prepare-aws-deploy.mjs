#!/usr/bin/env node
/**
 * Build AWS deploy packages from the monorepo root.
 *
 * Output:
 *   aws-deploy/dist/frontend/   — UI EC2 package (port 3000)
 *   aws-deploy/dist/backend/    — API EC2 package (port 3001)
 *   aws-deploy/dist/monolith/   — single EC2 package (recommended)
 *
 * Usage: node scripts/prepare-aws-deploy.mjs
 *        pnpm run prepare:aws
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const AWS_DEPLOY = path.join(ROOT, 'aws-deploy');
const DIST = path.join(AWS_DEPLOY, 'dist');

const manifest = JSON.parse(
  fs.readFileSync(path.join(AWS_DEPLOY, 'shared', 'manifest.json'), 'utf8'),
);
const rootPkg = JSON.parse(fs.readFileSync(path.join(ROOT, 'package.json'), 'utf8'));

const PACKAGES = [
  { id: 'frontend', label: 'Frontend (UI)' },
  { id: 'backend', label: 'Backend (API + RDS)' },
  { id: 'monolith', label: 'Monolith (UI + API)' },
];

function rmDir(dir) {
  if (fs.existsSync(dir)) fs.rmSync(dir, { recursive: true, force: true });
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function copyFile(src, dest) {
  ensureDir(path.dirname(dest));
  fs.copyFileSync(src, dest);
}

function copyDir(src, dest, excludeNames = new Set()) {
  if (!fs.existsSync(src)) return;
  ensureDir(dest);
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    if (excludeNames.has(entry.name)) continue;
    const s = path.join(src, entry.name);
    const d = path.join(dest, entry.name);
    if (entry.isDirectory()) copyDir(s, d, excludeNames);
    else copyFile(s, d);
  }
}

function copyDeployTemplate(pkgId, destRoot) {
  const tpl = path.join(AWS_DEPLOY, pkgId);
  const deployDest = path.join(destRoot, 'deploy');
  ensureDir(deployDest);
  for (const name of ['deploy.sh', 'ecosystem.config.cjs', 'nginx.conf']) {
    const src = path.join(tpl, name);
    if (fs.existsSync(src)) copyFile(src, path.join(deployDest, name));
  }
  const envExample = path.join(tpl, 'env.example');
  if (fs.existsSync(envExample)) {
    copyFile(envExample, path.join(destRoot, '.env.aws.example'));
  }
}

function mergePackageJson(pkgId, destRoot) {
  const tpl = JSON.parse(
    fs.readFileSync(path.join(AWS_DEPLOY, pkgId, 'package.json'), 'utf8'),
  );
  const merged = {
    ...tpl,
    dependencies: rootPkg.dependencies,
    devDependencies: rootPkg.devDependencies,
  };
  fs.writeFileSync(
    path.join(destRoot, 'package.json'),
    JSON.stringify(merged, null, 2) + '\n',
  );
}

function writeReadme(pkgId, destRoot) {
  const lines = [
    `# PrepIndia AWS — ${pkgId}`,
    '',
    'Generated by `pnpm run prepare:aws`. Upload this entire folder to EC2.',
    '',
    '```bash',
    'cp .env.aws.example .env.production   # edit RDS + secrets',
    'pnpm install --frozen-lockfile',
    'chmod +x deploy/deploy.sh',
    './deploy/deploy.sh',
    '```',
    '',
    'See `aws-deploy/README.md` in the source repo for nginx and split-EC2 setup.',
    '',
  ];
  fs.writeFileSync(path.join(destRoot, 'README-DEPLOY.md'), lines.join('\n'));
}

function buildPackage(pkgId) {
  const dest = path.join(DIST, pkgId);
  console.log(`\n▶ Building aws-deploy/dist/${pkgId}/`);
  rmDir(dest);
  ensureDir(dest);

  for (const dir of manifest.includeDirs) {
    const src = path.join(ROOT, dir);
    copyDir(src, path.join(dest, dir));
  }

  for (const file of manifest.includeFiles) {
    const src = path.join(ROOT, file);
    if (fs.existsSync(src)) copyFile(src, path.join(dest, file));
  }

  mergePackageJson(pkgId, dest);
  copyDeployTemplate(pkgId, dest);
  writeReadme(pkgId, dest);

  console.log(`  ✓ ${dest}`);
}

console.log('═══ PrepIndia AWS deploy packager ═══');
console.log(`Source: ${ROOT}`);

rmDir(DIST);
ensureDir(DIST);

for (const pkg of PACKAGES) {
  buildPackage(pkg.id);
}

console.log('\n✅ Done. Upload folders from aws-deploy/dist/ to EC2:\n');
for (const pkg of PACKAGES) {
  console.log(`   • ${pkg.label} → aws-deploy/dist/${pkg.id}/`);
}
console.log('\nRecommended for trial: aws-deploy/dist/monolith/\n');
